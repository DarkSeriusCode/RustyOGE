fn to_map(raw_map: &[(&str, &[&str])]) -> HashMap<String, Vec<String>> {
    let mut map = HashMap::new();
    for (key, val) in raw_map {
        map.insert(key.to_string(), Vec::from_iter(val.iter().map(|s| s.to_string())));
    }
    map
}

